# 1. Identity: (a + b)^2 = a^2 + 2ab + b^2
print("--- Testing (a + b)^2 ---")
a = int(input("Enter the value of a: "))
b = int(input("Enter the value of b: "))

lhs = (a + b) * (a + b)
rhs = (a * a) + (2 * a * b) + (b * b)

print("LHS =", lhs)
print("RHS =", rhs)
print("Proved:", lhs == rhs)
print("-" * 25)

# 2. Identity: (a - b)^2 = a^2 - 2ab + b^2
print("\n--- Testing (a - b)^2 ---")
a = int(input("Enter the value of a: "))
b = int(input("Enter the value of b: "))

lhs = (a - b) * (a - b)
rhs = (a * a) - (2 * a * b) + (b * b)

print("LHS =", lhs)
print("RHS =", rhs)
print("Proved:", lhs == rhs)
print("-" * 25)

# 3. Identity: (a + b)(a - b) = a^2 - b^2
print("\n--- Testing (a + b)(a - b) ---")
a = int(input("Enter the value of a: "))
b = int(input("Enter the value of b: "))

lhs = (a + b) * (a - b)
rhs = (a * a) - (b * b)

print("LHS =", lhs)
print("RHS =", rhs)
print("Proved:", lhs == rhs)
print("-" * 25)
